-- Fase 6 del modulo de nominas: PERSISTIR la nomina emitida.
--
-- Hasta ahora todo se calculaba al vuelo con calcular_nomina_persona. Eso hace
-- que una nomina "ya entregada" cambie sola si manana se corrige una tarifa de
-- convenio, un complemento o una regla del motor. Aqui se congela: al emitir se
-- guarda la cabecera con sus totales y TODAS las lineas del desglose tal cual
-- salieron ese dia, junto con los parametros usados para calcularla.
--
-- A partir de la emision, la nomina se LEE de estas tablas; el motor solo se
-- vuelve a tocar si se emite otra (reemplazando a la anterior, que queda
-- anulada con rastro).
--
-- Una persona puede tener VARIAS nominas en el mismo mes: dos vidas laborales
-- que no se solapan son dos nominas reales (ver p_historial_ids en
-- nomina_calculo_persona.sql). Por eso la clave de unicidad incluye el conjunto
-- de historiales, no solo persona + periodo.
--
-- NO ES SOLO EL RECIBO: una nomina emitida es tambien el REGISTRO LABORAL del
-- periodo. Ademas de las lineas de importe, se guarda:
--   * nomina_historiales -- una fila por periodo/puesto que entra en la nomina,
--     con la foto completa del contrato en ese momento (convenio y tarifas,
--     jornada, coeficiente, modalidad de pago, horas teoricas, flags de pluses,
--     tipos de cotizacion). Es lo que explica de donde sale cada importe.
--   * nomina_horas -- las horas realmente trabajadas por puesto, desglosadas
--     por tipo de hora y situacion, con los ids de los registros de origen.
--   * nomina_lineas -- ademas de la nomina de la persona (ambito 'persona'),
--     el desglose POR PUESTO (ambito 'puesto'), con cantidad y precio unitario
--     de cada plus y complemento.
-- Con eso, la nomina se sostiene sola aunque despues se corrijan registros,
-- tarifas o el propio historial laboral.

-- ============================================================================
-- Cabecera
-- ============================================================================

create table if not exists public.nominas (
  id bigint generated by default as identity primary key,
  personal_id integer not null references public.personal (id),
  empresa_id integer references public.empresas (id),
  periodo_desde date not null,
  periodo_hasta date not null,
  -- Periodos de historial laboral incluidos. Se guarda ordenado (trigger) para
  -- que el indice de unicidad no vea distintas dos listas con los mismos ids.
  historial_ids bigint[] not null default '{}'::bigint[],

  -- Mes natural al que corresponde, cuando el periodo cae entero dentro de uno.
  -- Nulo en rangos a caballo entre dos meses. Sirve para listar "las nominas de
  -- junio 2026" sin tener que adivinarlo desde las fechas.
  ejercicio integer generated always as (
    case when extract(year from periodo_desde) = extract(year from periodo_hasta)
          and extract(month from periodo_desde) = extract(month from periodo_hasta)
    then extract(year from periodo_desde)::integer end
  ) stored,
  mes integer generated always as (
    case when extract(year from periodo_desde) = extract(year from periodo_hasta)
          and extract(month from periodo_desde) = extract(month from periodo_hasta)
    then extract(month from periodo_desde)::integer end
  ) stored,

  -- Parametros con los que se calculo. No se usan para nada al leer: estan para
  -- poder auditar y reproducir el calculo (o repetirlo con los mismos ajustes).
  base_calculo text,
  ajuste_jornada text,
  manual_importe numeric(12, 2),
  manual_modo text,
  manual_pagas_incluidas boolean not null default false,
  manual_complementos bigint[],
  manual_transporte boolean not null default false,

  -- Totales congelados (se derivan de nomina_lineas al emitir).
  total_devengado numeric(12, 2) not null default 0,
  base_cc numeric(12, 2) not null default 0,
  base_cp numeric(12, 2) not null default 0,
  base_irpf numeric(12, 2) not null default 0,
  total_deducciones numeric(12, 2) not null default 0,
  liquido numeric(12, 2) not null default 0,

  estado text not null default 'emitida',
  -- Emitida con las lineas ajustadas a mano (p_lineas) en vez de con las que
  -- dio el motor. El expediente se congela igual: lo trabajado no se edita.
  editada boolean not null default false,
  notas text,
  anulada_motivo text,
  anulada_en timestamptz,
  -- Nomina anterior a la que reemplaza esta (al reemitir).
  sustituye_a bigint references public.nominas (id),

  emitida_en timestamptz not null default now(),
  emitida_por uuid default auth.uid(),
  emitida_por_email text,
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now(),

  constraint nominas_periodo_valido_chk check (periodo_hasta >= periodo_desde),
  constraint nominas_estado_chk check (estado in ('emitida', 'anulada')),
  constraint nominas_manual_modo_chk
    check (manual_modo is null or manual_modo in ('periodo', 'diario', 'hora')),
  constraint nominas_manual_coherencia_chk
    check (manual_importe is not null or manual_modo is null)
);

comment on table public.nominas is
  'Nomina emitida y congelada: cabecera con totales y parametros de calculo. El desglose vive en nomina_lineas. Emitir de nuevo el mismo periodo anula la anterior (sustituye_a).';
comment on column public.nominas.historial_ids is
  'Periodos de historiales_laborales incluidos, ordenados. Dos vidas laborales que no se solapan dan DOS nominas del mismo mes, cada una con su lista.';
comment on column public.nominas.estado is
  'emitida = vigente; anulada = reemplazada o cancelada a mano (conserva sus lineas como historico).';

-- Una sola nomina viva por persona + empresa + periodo + conjunto de
-- historiales. Las anuladas quedan fuera para poder reemitir.
create unique index if not exists nominas_vigente_unica_idx
on public.nominas (personal_id, coalesce(empresa_id, 0), periodo_desde, periodo_hasta, historial_ids)
where estado = 'emitida';

create index if not exists nominas_personal_periodo_idx
on public.nominas (personal_id, periodo_desde desc, periodo_hasta desc);

create index if not exists nominas_periodo_idx
on public.nominas (ejercicio, mes);

-- ============================================================================
-- Lineas del desglose: copia literal de lo que devolvio calcular_nomina_persona
-- ============================================================================

create table if not exists public.nomina_lineas (
  id bigint generated by default as identity primary key,
  nomina_id bigint not null references public.nominas (id) on delete cascade,
  -- 'persona' = la nomina real (la que suma). 'puesto' = el desglose de UN
  -- historial, con historial_id relleno; es explicativo y NO suma a los totales.
  ambito text not null default 'persona',
  historial_id bigint,
  orden integer not null,
  seccion text not null,
  concepto text not null,
  detalle text,
  base numeric(14, 4),
  tipo numeric(10, 6),
  -- Unidades y precio unitario que produjeron el importe (dias x tarifa de
  -- transporte, horas x precio hora, 1 x importe fijo). El texto de `detalle` lo
  -- cuenta en prosa; esto es lo explotable.
  cantidad numeric(14, 4),
  precio numeric(14, 6),
  -- Codigo del programa de nominas externo (398 transporte, 93 disponibilidad,
  -- 60 complemento de puesto, 67 complementarias, 12 festivo...), cuando se
  -- conoce. Ver get_codigo_nomina_concepto.
  codigo_nomina integer,
  importe numeric(12, 2),
  -- Igual que en el motor: si viene relleno, esta linea DESGLOSA a la anterior
  -- sin detalle_de y por tanto NO suma (si sumara, se contaria dos veces).
  detalle_de text,
  constraint nomina_lineas_ambito_chk check (ambito in ('persona', 'puesto')),
  constraint nomina_lineas_historial_chk
    check (ambito = 'persona' or historial_id is not null)
);

comment on table public.nomina_lineas is
  'Desglose congelado de una nomina emitida. ambito=persona son las lineas de la nomina real; ambito=puesto es el desglose por historial (explicativo, no suma).';
comment on column public.nomina_lineas.detalle_de is
  'Si no es nulo, la linea desglosa a la anterior sin detalle_de y NO suma al bruto ni a las bases.';
comment on column public.nomina_lineas.ambito is
  'persona = suma en la nomina; puesto = detalle del historial indicado en historial_id, NO suma.';

create index if not exists nomina_lineas_nomina_idx
on public.nomina_lineas (nomina_id, ambito, orden);

create index if not exists nomina_lineas_historial_idx
on public.nomina_lineas (nomina_id, historial_id);

