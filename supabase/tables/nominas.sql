create or replace function public.emitir_nomina(
  p_personal_id integer,
  p_desde date,
  p_hasta date,
  p_empresa_id integer default null,
  p_base_calculo text default null,
  p_ajuste_jornada text default null,
  p_historial_ids bigint[] default null,
  p_manual_importe numeric default null,
  p_manual_modo text default null,
  p_manual_pagas_incluidas boolean default false,
  p_manual_complementos bigint[] default null,
  p_manual_transporte boolean default false,
  p_notas text default null,
  p_reemplazar boolean default false,
  -- Lineas ajustadas a mano desde el editor de Gestion. Nulo = las que calcule
  -- el motor. Cada elemento: {orden, seccion, concepto, detalle, base, tipo,
  -- cantidad, precio, importe}. El expediente (nomina_historiales y
  -- nomina_horas) se congela igual: lo trabajado no se edita, solo lo que se
  -- paga por ello.
  p_lineas jsonb default null
)
returns bigint
language plpgsql
volatile
security invoker
set search_path = public
as $$
declare
  v_id bigint;
  v_previa bigint;
  v_lineas integer;
  v_ids bigint[];
  v_editada boolean := p_lineas is not null;
begin
  if not public.is_coordinacion_admin() then
    raise exception 'Solo un administrador puede emitir nóminas.';
  end if;
  if p_desde is null or p_hasta is null then
    raise exception 'La nómina necesita un periodo (desde y hasta).';
  end if;
  if v_editada and jsonb_typeof(p_lineas) <> 'array' then
    raise exception 'Las líneas editadas deben venir como una lista.';
  end if;

  -- Se normaliza aqui tambien para poder buscar la nomina previa con la misma
  -- lista de historiales que guardara el trigger.
  v_ids := coalesce((
    select array_agg(distinct x order by x)
    from unnest(coalesce(p_historial_ids, '{}'::bigint[])) as x
    where x is not null
  ), '{}'::bigint[]);

  select n.id into v_previa
  from public.nominas n
  where n.personal_id = p_personal_id
    and coalesce(n.empresa_id, 0) = coalesce(p_empresa_id, 0)
    and n.periodo_desde = p_desde
    and n.periodo_hasta = p_hasta
    and n.historial_ids = v_ids
    and n.estado = 'emitida';

  if v_previa is not null then
    if not p_reemplazar then
      raise exception 'Ya hay una nómina emitida para este periodo (#%). Anúlala o emite reemplazándola.', v_previa;
    end if;
    update public.nominas
    set estado = 'anulada', anulada_en = now(),
        anulada_motivo = coalesce(anulada_motivo, 'Reemplazada al reemitir el periodo')
    where id = v_previa;
  end if;

  insert into public.nominas (
    personal_id, empresa_id, periodo_desde, periodo_hasta, historial_ids,
    base_calculo, ajuste_jornada,
    manual_importe, manual_modo, manual_pagas_incluidas,
    manual_complementos, manual_transporte,
    notas, sustituye_a, editada, emitida_por_email
  ) values (
    p_personal_id, p_empresa_id, p_desde, p_hasta, v_ids,
    p_base_calculo, p_ajuste_jornada,
    p_manual_importe, p_manual_modo, coalesce(p_manual_pagas_incluidas, false),
    p_manual_complementos, coalesce(p_manual_transporte, false),
    -- El nullif exterior evita reventar cuando el claim no viene (cadena vacia
    -- no es jsonb valido).
    p_notas, v_previa, v_editada,
    nullif(nullif(current_setting('request.jwt.claims', true), '')::jsonb ->> 'email', '')
  )
  returning id into v_id;

  -- 1) La nomina de la persona: las lineas que suman. Si vienen editadas se
  --    guardan tal cual; si no, las que calcule el motor.
  if v_editada then
    insert into public.nomina_lineas (
      nomina_id, ambito, orden, seccion, concepto, detalle,
      base, tipo, cantidad, precio, codigo_nomina, importe, detalle_de)
    select v_id, 'persona',
           coalesce((l->>'orden')::integer, 0),
           coalesce(nullif(l->>'seccion', ''), 'devengo'),
           coalesce(nullif(l->>'concepto', ''), 'Sin concepto'),
           nullif(l->>'detalle', ''),
           (l->>'base')::numeric, (l->>'tipo')::numeric,
           (l->>'cantidad')::numeric, (l->>'precio')::numeric,
           public.get_codigo_nomina_concepto(l->>'concepto'),
           round(coalesce((l->>'importe')::numeric, 0), 2),
           nullif(l->>'detalle_de', '')
    from jsonb_array_elements(p_lineas) l;
  else
    insert into public.nomina_lineas (
      nomina_id, ambito, orden, seccion, concepto, detalle,
      base, tipo, cantidad, precio, codigo_nomina, importe, detalle_de)
    select v_id, 'persona', c.orden, c.seccion, c.concepto, c.detalle,
           c.base, c.tipo, c.cantidad, c.precio,
           public.get_codigo_nomina_concepto(c.concepto), c.importe, c.detalle_de
    from public.calcular_nomina_persona(
      p_personal_id, p_desde, p_hasta, p_empresa_id, p_base_calculo, p_ajuste_jornada,
      nullif(v_ids, '{}'::bigint[]), p_manual_importe, p_manual_modo,
      coalesce(p_manual_pagas_incluidas, false), p_manual_complementos,
      coalesce(p_manual_transporte, false)
    ) c;
  end if;

  get diagnostics v_lineas = row_count;
  if v_lineas = 0 then
    raise exception 'El cálculo no devuelve ninguna línea para este periodo: no hay nada que emitir.';
  end if;

  -- 2) El contexto laboral de cada periodo/puesto que entra en la nomina.
  --    c.* se expande en el orden del returns table de la funcion, que es el
  --    mismo en el que se listan las columnas destino.
  insert into public.nomina_historiales (
    nomina_id,
    historial_id, personal_id, empresa_id, empresa, puesto_id, puesto,
    convenio_categoria_id, convenio, fecha_alta, fecha_baja,
    tramo_desde, tramo_hasta, dias_nomina, dias_trabajados,
    jornada, jornada_maxima, coeficiente_temporalidad_miles, horas_teoricas,
    modalidad_pago_id, modalidad_pago, ajuste_jornada, base_calculo, grupo_cotizacion,
    salario_mensual, salario_convenio_mensual, salario_convenio_diario,
    salario_convenio_hora, pagas_anuales, pagas_extra,
    horas_reg, horas_pnr, horas_mont, horas_hcomp, horas_ftrab,
    horas_bolsa_salida, horas_bolsa_entrada, horas_nocturnas,
    tiene_plus_transporte, tiene_nocturnidad,
    tiene_complemento_movilidad, tiene_complemento_dedicacion,
    tarifa_plus_transporte, tarifa_hora_nocturna,
    tarifa_complemento_movilidad_pct, tarifa_complemento_dedicacion,
    precio_hora_montaje, precio_hora_complementaria, precio_hora_jornada_completa,
    cotizacion_comunes_pct, cotizacion_mei_pct,
    cotizacion_desempleo_pct, cotizacion_formacion_pct)
  select v_id, c.*
  from public.historiales_laborales h
  cross join lateral public.get_nomina_contexto_historial(
    h.id, p_desde, p_hasta, p_base_calculo, p_ajuste_jornada) c
  where h.personal_id = p_personal_id
    and (p_empresa_id is null or h.empresa_id = p_empresa_id)
    and (v_ids = '{}'::bigint[] or h.id = any(v_ids))
    and h.fecha_alta <= p_hasta and (h.fecha_baja is null or h.fecha_baja >= p_desde);

  -- El predominante (mas dias en el periodo) aporta los tipos de cotizacion y
  -- el numero de pagas del total, igual que hace calcular_nomina_persona.
  update public.nomina_historiales nh
  set predominante = true
  where nh.id = (
    select nh2.id from public.nomina_historiales nh2
    where nh2.nomina_id = v_id
    order by (least(coalesce(nh2.fecha_baja, p_hasta), p_hasta)
              - greatest(nh2.fecha_alta, p_desde)) desc, nh2.historial_id
    limit 1);

  -- 3) El desglose por puesto: explica de donde sale cada importe del total.
  --    NO suma (ambito 'puesto'); los totales se calculan solo con 'persona'.
  insert into public.nomina_lineas (
    nomina_id, ambito, historial_id, orden, seccion, concepto, detalle,
    base, tipo, cantidad, precio, codigo_nomina, importe, detalle_de)
  select v_id, 'puesto', h.id, d.orden, 'devengo', d.concepto, d.detalle,
         d.base, null::numeric, d.cantidad, d.precio,
         public.get_codigo_nomina_concepto(d.concepto), d.importe, d.detalle_de
  from public.historiales_laborales h
  cross join lateral public.calcular_nomina_devengos(
    h.id, p_desde, p_hasta, p_base_calculo, p_ajuste_jornada) d
  where h.personal_id = p_personal_id
    and (p_empresa_id is null or h.empresa_id = p_empresa_id)
    and (v_ids = '{}'::bigint[] or h.id = any(v_ids))
    and h.fecha_alta <= p_hasta and (h.fecha_baja is null or h.fecha_baja >= p_desde);

  update public.nomina_historiales nh
  set total_devengado = coalesce((
    select sum(l.importe) from public.nomina_lineas l
    where l.nomina_id = v_id and l.ambito = 'puesto'
      and l.historial_id = nh.historial_id and l.detalle_de is null), 0)
  where nh.nomina_id = v_id;

  -- 4) Las horas realmente trabajadas, por puesto x tipo de hora x situacion.
  --    Se guardan TAMBIEN las excluidas (CAMB/LG) para que el registro laboral
  --    este completo; van marcadas para que se vea que cuentan cero.
  insert into public.nomina_horas (
    nomina_id, historial_id, puesto_id, puesto,
    tipo_hora_id, tipo_hora, tipo_hora_codigo_nomina,
    situacion_id, situacion, horas, horas_nocturnas, dias, registros,
    registro_ids, excluida, cuenta_jornada, dia_efectivo)
  select v_id, h.id, h.puesto_id, pu.puesto,
         r.tipo_hora_id, th.tipo_hora, th.codigo_nomina,
         r.situacion_id, s.situacion,
         coalesce(sum(r.horas), 0)::numeric,
         coalesce(sum(r.horas_nocturnas), 0)::numeric,
         count(distinct r.fecha)::integer,
         count(*)::integer,
         array_agg(r.id order by r.fecha, r.id),
         coalesce(s.situacion in ('CAMB', 'LG'), false),
         r.tipo_hora_id in (1, 5, 8) and coalesce(s.situacion not in ('CAMB', 'LG'), true),
         coalesce(s.situacion in ('NORM', 'SUST')
                  or (s.situacion = 'FEST' and th.tipo_hora = 'FTRAB'), false)
  from public.historiales_laborales h
  join public.registros r
    on r.personal_id = h.personal_id and r.puesto_id = h.puesto_id
   and (h.empresa_id is null or r.empresa_id = h.empresa_id)
   and r.fecha >= greatest(h.fecha_alta, p_desde)
   and r.fecha <= least(coalesce(h.fecha_baja, p_hasta), p_hasta)
  left join public.tipo_horas th on th.id = r.tipo_hora_id
  left join public.situaciones s on s.id = r.situacion_id
  left join public.puestos pu on pu.id = h.puesto_id
  where h.personal_id = p_personal_id
    and (p_empresa_id is null or h.empresa_id = p_empresa_id)
    and (v_ids = '{}'::bigint[] or h.id = any(v_ids))
    and h.fecha_alta <= p_hasta and (h.fecha_baja is null or h.fecha_baja >= p_desde)
  group by h.id, h.puesto_id, pu.puesto, r.tipo_hora_id, th.tipo_hora,
           th.codigo_nomina, r.situacion_id, s.situacion;

  -- Los totales se derivan de las lineas, no se copian de las lineas-resumen del
  -- motor: asi la cabecera cuadra con su propio desglose. Las filas con
  -- detalle_de quedan fuera porque desglosan a otra que ya suma. Las bases no
  -- son sumables (son magnitudes, no conceptos), asi que se toman de su linea
  -- por `orden`: 600 comunes, 601 profesionales, 602 IRPF (ver
  -- nomina_calculo_persona.sql; si cambian alli, cambiar aqui).
  -- OJO: solo las lineas de ambito 'persona'. Las de 'puesto' son el mismo
  -- dinero explicado por contrato; sumarlas duplicaria el bruto.
  update public.nominas n set
    total_devengado = coalesce((
      select sum(l.importe) from public.nomina_lineas l
      where l.nomina_id = n.id and l.ambito = 'persona'
        and l.seccion = 'devengo' and l.detalle_de is null), 0),
    total_deducciones = coalesce((
      select sum(l.importe) from public.nomina_lineas l
      where l.nomina_id = n.id and l.ambito = 'persona'
        and l.seccion = 'deduccion' and l.detalle_de is null), 0),
    base_cc = coalesce((
      select l.importe from public.nomina_lineas l
      where l.nomina_id = n.id and l.ambito = 'persona' and l.orden = 600), 0),
    base_cp = coalesce((
      select l.importe from public.nomina_lineas l
      where l.nomina_id = n.id and l.ambito = 'persona' and l.orden = 601), 0),
    base_irpf = coalesce((
      select l.importe from public.nomina_lineas l
      where l.nomina_id = n.id and l.ambito = 'persona' and l.orden = 602), 0)
  where n.id = v_id;

  update public.nominas
  set liquido = round(total_devengado - total_deducciones, 2)
  where id = v_id;

  return v_id;
end;
$$;

