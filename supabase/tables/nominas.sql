-- Ordena historial_ids y quita duplicados/nulos: sin esto, {5863,5864} y
-- {5864,5863} serian dos nominas distintas para el indice de unicidad.
create or replace function public.normalize_nomina_historial_ids()
returns trigger
language plpgsql
set search_path = public
as $$
begin
  new.historial_ids := coalesce((
    select array_agg(distinct x order by x)
    from unnest(coalesce(new.historial_ids, '{}'::bigint[])) as x
    where x is not null
  ), '{}'::bigint[]);
  return new;
end;
$$;

drop trigger if exists normalize_nomina_historial_ids on public.nominas;
create trigger normalize_nomina_historial_ids
before insert or update on public.nominas
for each row
execute function public.normalize_nomina_historial_ids();

-- ============================================================================
-- Codigo del programa de nominas externo para un concepto del motor
-- ============================================================================

-- Los complementos traen el suyo en el catalogo; los conceptos que genera el
-- propio motor (y no son complementos asignables) van en el mapa fijo. Sus
-- codigos los dio el usuario y estan documentados en nomina_complementos_codigos.sql.
create or replace function public.get_codigo_nomina_concepto(p_concepto text)
returns integer
language sql
stable
security invoker
set search_path = public
as $$
  select coalesce(
    (select c.codigo_nomina
       from public.nomina_complementos_catalogo c
      where lower(c.nombre) = lower(trim(p_concepto))
      limit 1),
    case lower(trim(coalesce(p_concepto, '')))
      when 'plus de disponibilidad' then 93
      when 'horas complementarias' then 67
      when 'plus festivo trabajado' then 12
      when 'descuento por absentismo' then 790
      when 'prorrateo pagas extra' then 30
      when 'p.p. pagas extra (solo cotiza)' then 30
      else null
    end);
$$;

revoke all on function public.get_codigo_nomina_concepto(text) from public;
grant execute on function public.get_codigo_nomina_concepto(text) to authenticated;

-- ============================================================================
-- Contexto laboral de un periodo: la foto que explica su calculo
-- ============================================================================

-- ACOPLAMIENTO DELIBERADO con calcular_nomina_devengos (nomina_calculo.sql):
-- reproduce su preambulo -- ventana efectiva, resolucion del ajuste de jornada
-- desde la modalidad de pago, modo de base_calculo y recuento de horas por tipo
-- con las mismas exclusiones (CAMB y LG). No calcula importes: solo describe el
-- contrato y lo trabajado. Si alli cambia alguna de esas reglas, cambiarla aqui.
create or replace function public.get_nomina_contexto_historial(
  p_historial_id bigint,
  p_desde date default null,
  p_hasta date default null,
  p_base_calculo text default null,
  p_ajuste_jornada text default null
)
returns table (
  historial_id bigint, personal_id integer, empresa_id integer, empresa text,
  puesto_id integer, puesto text, convenio_categoria_id integer, convenio text,
  fecha_alta date, fecha_baja date, tramo_desde date, tramo_hasta date,
  dias_nomina integer, dias_trabajados integer,
  jornada numeric, jornada_maxima numeric, coeficiente_temporalidad_miles integer,
  horas_teoricas numeric, modalidad_pago_id integer, modalidad_pago text,
  ajuste_jornada text, base_calculo text, grupo_cotizacion smallint,
  salario_mensual numeric, salario_convenio_mensual numeric,
  salario_convenio_diario numeric, salario_convenio_hora numeric,
  pagas_anuales numeric, pagas_extra integer,
  horas_reg numeric, horas_pnr numeric, horas_mont numeric, horas_hcomp numeric,
  horas_ftrab numeric, horas_bolsa_salida numeric, horas_bolsa_entrada numeric,
  horas_nocturnas numeric,
  tiene_plus_transporte boolean, tiene_nocturnidad boolean,
  tiene_complemento_movilidad boolean, tiene_complemento_dedicacion boolean,
  tarifa_plus_transporte numeric, tarifa_hora_nocturna numeric,
  tarifa_complemento_movilidad_pct numeric, tarifa_complemento_dedicacion numeric,
  precio_hora_montaje numeric, precio_hora_complementaria numeric,
  precio_hora_jornada_completa numeric,
  cotizacion_comunes_pct numeric, cotizacion_mei_pct numeric,
  cotizacion_desempleo_pct numeric, cotizacion_formacion_pct numeric
)
language plpgsql
stable
security invoker
set search_path = public
as $$
declare
  h public.historiales_laborales;
  v_conv public.convenios_categorias_salarios;
  v_sal record;
  v_desde date; v_hasta date; v_fecha_ref date;
  v_convenio_id integer; v_ajuste text; v_modalidad text; v_modo text;
  v_sit_excluidas integer[];
begin
  select * into h from public.historiales_laborales where id = p_historial_id;
  if h.id is null then return; end if;

  select coalesce(array_agg(s.id), '{}'::integer[]) into v_sit_excluidas
  from public.situaciones s where s.situacion in ('CAMB', 'LG');

  v_desde := greatest(h.fecha_alta, coalesce(p_desde, h.fecha_alta));
  v_hasta := least(
    coalesce(h.fecha_baja, coalesce(p_hasta, current_date)),
    coalesce(p_hasta, coalesce(h.fecha_baja, current_date))
  );
  if v_hasta < v_desde then return; end if;
  v_fecha_ref := v_desde;

  v_ajuste := nullif(trim(p_ajuste_jornada), '');
  if v_ajuste is null then
    select lower(mp.modalidad_pago) into v_modalidad
    from public.historiales_laborales_modalidades_pago mp
    where mp.id = h.modalidad_pago_id;
    v_ajuste := case when v_modalidad = 'horas totales' then 'ambos' else 'ninguno' end;
  end if;

  select pu.convenio_id into v_convenio_id from public.puestos pu where pu.id = h.puesto_id;
  if v_convenio_id is not null then
    select * into v_conv from public.get_convenio_salario_vigente(v_convenio_id, v_fecha_ref);
  end if;
  select * into v_sal from public.get_puesto_salario_efectivo(h.puesto_id, v_fecha_ref);

  v_modo := coalesce(nullif(trim(p_base_calculo), ''), v_conv.base_calculo, 'mensual');

  return query
  select
    h.id, h.personal_id, h.empresa_id,
    (select e.empresa from public.empresas e where e.id = h.empresa_id),
    h.puesto_id,
    coalesce((select pu.puesto from public.puestos pu where pu.id = h.puesto_id), h.puesto_texto),
    v_convenio_id,
    (select cc.convenio || coalesce(' · ' || cc.grupo_nivel, '')
       from public.convenios_categorias cc where cc.id = v_convenio_id),
    h.fecha_alta, h.fecha_baja, v_desde, v_hasta,
    public.dias_nomina(v_desde, v_hasta),
    (select count(distinct r.fecha)::integer
       from public.registros r
       join public.situaciones s on s.id = r.situacion_id
       left join public.tipo_horas th on th.id = r.tipo_hora_id
      where r.personal_id = h.personal_id and r.puesto_id = h.puesto_id
        and (h.empresa_id is null or r.empresa_id = h.empresa_id)
        and r.fecha >= v_desde and r.fecha <= v_hasta
        and (s.situacion in ('NORM','SUST') or (s.situacion = 'FEST' and th.tipo_hora = 'FTRAB'))),
    h.jornada, h.jornada_maxima, h.coeficiente_temporalidad_miles,
    public.horas_teoricas_jornada(v_desde, v_hasta, h.jornada),
    h.modalidad_pago_id,
    (select mp.modalidad_pago from public.historiales_laborales_modalidades_pago mp
      where mp.id = h.modalidad_pago_id),
    v_ajuste, v_modo, h.grupo_cotizacion,
    v_sal.salario_mensual, v_conv.salario_mensual, v_conv.salario_diario, v_conv.salario_hora,
    v_conv.pagas_anuales,
    greatest(coalesce(v_conv.pagas_anuales, 12)::integer - 12, 0),
    r.horas_reg, r.horas_pnr, r.horas_mont, r.horas_hcomp, r.horas_ftrab,
    r.horas_bout, r.horas_bin, r.horas_noct,
    h.tiene_plus_transporte, h.tiene_nocturnidad,
    h.tiene_complemento_movilidad, h.tiene_complemento_dedicacion,
    v_conv.plus_transporte, v_conv.plus_hora_nocturna,
    v_conv.complemento_movilidad_pct, v_conv.complemento_dedicacion,
    public.get_puesto_precio_hora(h.puesto_id, 3, v_fecha_ref),
    public.get_puesto_precio_hora(h.puesto_id, 2, v_fecha_ref),
    public.get_convenio_precio_hora_jc(v_convenio_id, h.jornada_maxima, v_fecha_ref),
    h.cotizacion_comunes_pct, h.cotizacion_mei_pct,
    h.cotizacion_desempleo_pct, h.cotizacion_formacion_pct
  from (
    -- Mismas exclusiones que el motor: CAMB y LG no son trabajo de esta persona.
    select
      coalesce(sum(rg.horas) filter (where rg.tipo_hora_id = 1), 0)::numeric as horas_reg,
      coalesce(sum(rg.horas) filter (where rg.tipo_hora_id = 5), 0)::numeric as horas_pnr,
      coalesce(sum(rg.horas) filter (where rg.tipo_hora_id = 3), 0)::numeric as horas_mont,
      coalesce(sum(rg.horas) filter (where rg.tipo_hora_id = 2), 0)::numeric as horas_hcomp,
      coalesce(sum(rg.horas) filter (where rg.tipo_hora_id = 4), 0)::numeric as horas_ftrab,
      coalesce(sum(rg.horas) filter (where rg.tipo_hora_id = 8), 0)::numeric as horas_bout,
      coalesce(sum(rg.horas) filter (where rg.tipo_hora_id = 7), 0)::numeric as horas_bin,
      coalesce(sum(rg.horas_nocturnas), 0)::numeric as horas_noct
    from public.registros rg
    where rg.personal_id = h.personal_id and rg.puesto_id = h.puesto_id
      and (h.empresa_id is null or rg.empresa_id = h.empresa_id)
      and coalesce(rg.situacion_id, -1) <> all(v_sit_excluidas)
      and rg.fecha >= v_desde and rg.fecha <= v_hasta
  ) r;
end;
$$;

