comment on table public.registros is
  'Detalle diario/horario generado desde actividades. Cada fila puede enlazarse con actividad, contrato, personal, instalacion, puesto y catalogos horarios.';

comment on column public.registros.actividad_id is
  'Actividad de planificacion que origino el registro.';

comment on column public.registros.servicio_id is
  'Servicio asociado al registro. Se copia desde la actividad generadora cuando existe.';

comment on column public.registros.personal_id is
  'Personal principal asignado al registro.';

comment on column public.registros.titular_personal_id is
  'Personal titular cuando el registro representa una sustitucion.';

comment on column public.registros.sustituto_personal_id is
  'Personal sustituto cuando el registro representa una sustitucion.';

drop view if exists public.registros_detalle;

create or replace view public.registros_detalle as
select
  r.id,
  r.fecha,
  r.actividad_id,
  r.servicio_id,
  se.servicio,
  r.empresa_id,
  e.empresa,
  r.contrato_id,
  c.contrato,
  r.personal_id,
  p.personal,
  p.dni,
  r.titular_personal_id,
  titular.personal as titular_personal,
  r.sustituto_personal_id,
  sustituto.personal as sustituto_personal,
  r.instalacion_id,
  i.instalacion,
  r.categoria_id,
  r.puesto_id,
  pu.puesto,
  r.funcion_id,
  f.funcion,
  r.modalidad_id,
  m.modalidad,
  r.nivel_id,
  r.grupo_id,
  r.nota,
  r.dia_id,
  r.hora_inicio,
  r.hora_fin,
  r.horas,
  r.hc,
  r.hf,
  r.hm,
  r.hd,
  r.bolsa_horas,
  r.horas_diurnas,
  r.horas_nocturnas,
  r.clases,
  r.horas_2,
  r.descanso,
  r.activo,
  r.festivo,
  r.sustitucion,
  r.facturar,
  r.abonar,
  r.tipo_hora_id,
  th.tipo_hora,
  r.situacion_id,
  s.situacion,
  r.anio,
  r.observacion,
  r.control,
  r.factura
from public.registros r
left join public.actividades a
  on a.id = r.actividad_id
left join public.servicios se
  on se.id = r.servicio_id
left join public.empresas e
  on e.id = r.empresa_id
left join public.contratos c
  on c.id = r.contrato_id
left join public.personal p
  on p.id = r.personal_id
left join public.personal titular
  on titular.id = r.titular_personal_id
left join public.personal sustituto
  on sustituto.id = r.sustituto_personal_id
left join public.instalaciones i
  on i.id = r.instalacion_id
left join public.puestos pu
  on pu.id = r.puesto_id
left join public.funciones f
  on f.id = r.funcion_id
left join public.modalidades m
  on m.id = r.modalidad_id
left join public.tipo_horas th
  on th.id = r.tipo_hora_id
left join public.situaciones s
  on s.id = r.situacion_id;

alter view public.registros_detalle set (security_invoker = true);

grant select on public.registros_detalle to authenticated;
