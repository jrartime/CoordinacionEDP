do $$
begin
  if to_regclass('public.registros_horarios') is null
     and to_regclass('public.registros') is not null
     and exists (
       select 1
       from information_schema.columns
       where table_schema = 'public'
         and table_name = 'registros'
         and column_name = 'dni'
     ) then
    alter table public.registros rename to registros_horarios;
  end if;

  if to_regclass('public.registros') is null
     and to_regclass('public.horas') is not null then
    alter table public.horas rename to registros;
  end if;
end $$;
create table if not exists public.registros (
  id bigint generated by default as identity primary key,
  fecha date,
  actividad_id bigint,
  servicio_id bigint,
  empresa_id integer default 1,
  contrato_id integer,
  personal_id integer,
  titular_personal_id integer,
  sustituto_personal_id integer,
  instalacion_id integer,
  categoria_id integer,
  puesto_id integer,
  funcion_id integer,
  modalidad_id integer,
  nivel_id integer,
  grupo_id integer,
  nota text,
  dia_id integer,
  hora_inicio time,
  hora_fin time,
  horas real default 0,
  hc real default 0,
  hf real default 0,
  hm real default 0,
  hd real default 0,
  bolsa_horas real default 0,
  horas_diurnas real,
  horas_nocturnas real,
  clases real default 0,
  horas_2 real default 0,
  descanso boolean default false,
  activo boolean default true,
  festivo boolean default false,
  sustitucion boolean default false,
  facturar boolean default true,
  abonar boolean default true,
  tipo_hora_id integer,
  situacion_id integer default 1,
  anio integer default 0,
  observacion text,
  control timestamp with time zone default now(),
  factura varchar(10)
);

