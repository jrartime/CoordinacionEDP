-- Catalogo de convenios colectivos y categorias laborales (fuente: tbl_convenios.xlsx),
-- con su tabla salarial historica por fecha (mismo patron que puestos_salarios).
--
-- puestos.convenio_grupo_nivel ya contenia ids que coinciden exactamente con el Id de
-- este Excel: se renombra a convenio_id y se referencia a convenios_categorias(id).
--
-- convenios_categorias es un catalogo (identidad: nombre del convenio + nivel/categoria),
-- legible por cualquier autenticado como el resto de catalogos (puestos, funciones...).
-- convenios_categorias_salarios son las tarifas reales del convenio en el tiempo:
-- confidencial, admin-only, igual que puestos_salarios.

create table if not exists public.convenios_categorias (
  id integer primary key,
  convenio text not null,
  nivel text,
  grupo_nivel text,
  enlace text,
  activo boolean not null default true
);

comment on table public.convenios_categorias is
  'Categorias/niveles salariales de cada convenio colectivo (identidad estable). Las tarifas viven en convenios_categorias_salarios.';
comment on column public.convenios_categorias.nivel is
  'Descripcion textual de la categoria/nivel dentro del convenio (equivalente a la categoria laboral).';
comment on column public.convenios_categorias.grupo_nivel is
  'Codigo de grupo y nivel tal como aparece en el convenio (p.ej. "4.2 Instalaciones").';

create table if not exists public.convenios_categorias_salarios (
  id bigint generated by default as identity primary key,
  convenio_categoria_id integer not null references public.convenios_categorias (id),
  vigente_desde date not null,
  salario_mensual numeric(12, 2),
  salario_anual numeric(12, 2),
  pagas_anuales numeric(4, 1),
  hora_complementaria numeric(10, 4),
  plus_transporte numeric(10, 2),
  hora_montaje numeric(10, 4),
  plus_hora_nocturna numeric(10, 4),
  complemento_movilidad_pct numeric(6, 4),
  complemento_dedicacion numeric(12, 2),
  notas text,
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now(),
  constraint convenios_categorias_salarios_vigencia_unique
    unique (convenio_categoria_id, vigente_desde)
);

comment on table public.convenios_categorias_salarios is
  'Tarifas salariales del convenio por categoria, con vigencia por fecha. La tarifa vigente en una fecha es la de mayor vigente_desde <= esa fecha.';
comment on column public.convenios_categorias_salarios.complemento_movilidad_pct is
  'Complemento de movilidad como fraccion (0.10 = 10%), no importe fijo.';
comment on column public.convenios_categorias_salarios.complemento_dedicacion is
  'Complemento de dedicacion como importe fijo en euros.';

create index if not exists convenios_categorias_salarios_categoria_idx
on public.convenios_categorias_salarios (convenio_categoria_id, vigente_desde desc);

create or replace function public.set_convenios_categorias_salarios_updated_at()
returns trigger
language plpgsql
set search_path = public
as $$
begin
  new.updated_at = now();
  return new;
end;
$$;

drop trigger if exists set_convenios_categorias_salarios_updated_at on public.convenios_categorias_salarios;
create trigger set_convenios_categorias_salarios_updated_at
before update on public.convenios_categorias_salarios
for each row
execute function public.set_convenios_categorias_salarios_updated_at();

-- Tarifa de convenio vigente de una categoria en una fecha dada.
create or replace function public.get_convenio_salario_vigente(
  p_convenio_categoria_id integer,
  p_fecha date default current_date
)
returns public.convenios_categorias_salarios
language sql
stable
set search_path = public
as $$
  select cs.*
  from public.convenios_categorias_salarios cs
  where cs.convenio_categoria_id = p_convenio_categoria_id
    and cs.vigente_desde <= p_fecha
  order by cs.vigente_desde desc
  limit 1;
$$;

revoke all on function public.get_convenio_salario_vigente(integer, date) from public;
grant execute on function public.get_convenio_salario_vigente(integer, date) to authenticated;

