  end if;
end $$;

-- Precio/hora efectivo: ahora tambien cae al convenio (hora_complementaria/hora_montaje)
-- cuando el puesto no tiene tarifa propia ni override, ademas de puestos_salarios.
create or replace function public.get_puesto_precio_hora(
  p_puesto_id integer,
  p_tipo_hora_id integer,
  p_fecha date default current_date
)
returns numeric
language sql
stable
set search_path = public
as $$
  select coalesce(
    (
      select t.precio_hora
      from public.puestos_tarifas_hora t
      where t.puesto_id = p_puesto_id
        and t.tipo_hora_id = p_tipo_hora_id
        and t.vigente_desde <= p_fecha
      order by t.vigente_desde desc
      limit 1
    ),
    (
      select s.precio_hora * th.multiplicador
      from public.puestos_salarios s
      cross join public.tipo_horas th
      where s.puesto_id = p_puesto_id
        and th.id = p_tipo_hora_id
        and s.precio_hora is not null
        and s.vigente_desde <= p_fecha
      order by s.vigente_desde desc
      limit 1
    ),
    (
      select case p_tipo_hora_id
        when 2 then cs.hora_complementaria
        when 3 then cs.hora_montaje
        else null
      end
      from public.puestos p
      join public.convenios_categorias_salarios cs
        on cs.convenio_categoria_id = p.convenio_id
       and cs.vigente_desde <= p_fecha
      where p.id = p_puesto_id
      order by cs.vigente_desde desc
      limit 1
    )
  );
$$;

-- Salario mensual/anual efectivo: tarifa propia del puesto (puestos_salarios) si existe,
-- si no la del convenio asociado (convenios_categorias_salarios).
create or replace function public.get_puesto_salario_efectivo(
  p_puesto_id integer,
  p_fecha date default current_date
)
returns table (
  salario_mensual numeric,
  salario_anual numeric,
  origen text
)
language sql
stable
set search_path = public
as $$
  select
    coalesce(pr.salario_mensual, cr.salario_mensual) as salario_mensual,
    coalesce(pr.salario_anual, cr.salario_anual) as salario_anual,
    case when pr.salario_mensual is not null or pr.salario_anual is not null
      then 'puesto' else 'convenio' end as origen
  from (select 1) d
  left join public.get_puesto_salario_vigente(p_puesto_id, p_fecha) pr on true
  left join public.puestos p on p.id = p_puesto_id
  left join lateral (
    select ccs.salario_mensual, ccs.salario_anual
    from public.convenios_categorias_salarios ccs
    where ccs.convenio_categoria_id = p.convenio_id
      and ccs.vigente_desde <= p_fecha
    order by ccs.vigente_desde desc
    limit 1
  ) cr on true;
$$;

