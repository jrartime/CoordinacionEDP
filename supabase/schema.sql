create or replace function public.get_programming_personnel_for_pin(access_pin text)
returns table(name text)
language sql
stable
security definer
set search_path = public
as $$
  select programming_personnel.name
  from public.programming_personnel
  where programming_personnel.pin = btrim(access_pin)
    and programming_personnel.pin ~ '^[0-9]{8}$'
  order by programming_personnel.name
  limit 1
$$;

