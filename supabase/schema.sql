create or replace function public.get_candidate_filter_options()
returns table (
  option_type text,
  option_value text
)
language sql
stable
security definer
set search_path = public
as $$
  select 'role' as option_type, role as option_value
  from (
    select distinct unnest(job_roles) as role
    from public.candidates
  ) roles
  where nullif(btrim(role), '') is not null
  union
  select 'tag' as option_type, tag as option_value
  from (
    select distinct unnest(tags) as tag
    from public.candidates
  ) tags
  where nullif(btrim(tag), '') is not null
  order by option_type, option_value;
$$;

revoke all on function public.get_candidate_filter_options() from public;
grant execute on function public.get_candidate_filter_options() to authenticated;

